    private void cmbMATeacherItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbMATeacherItemStateChanged
        if(evt.getStateChange()==ItemEvent.SELECTED) {
            if(cmbMATeacher.getSelectedItem().toString().equals("Khongthep Boonmee")) {
                lblMATeacherPic.setIcon(imageKhongthep);
                DefaultComboBoxModel model = new DefaultComboBoxModel();
                model.addElement("- Select available time -");
                model.addElement("Mon 12:00");
                model.addElement("Mon 16:00");
                model.addElement("Tue 12:00");
                model.addElement("Wed 09:30");
                model.addElement("Wed 16:00");
                model.addElement("Fri 09:30");
                cmbMATeacherTime.setModel(model);
            }
            
            if(cmbMATeacher.getSelectedItem().toString().equals("Uraiwan Inyaem")) {
                lblMATeacherPic.setIcon(imageUraiwan);
                DefaultComboBoxModel model = new DefaultComboBoxModel();
                model.addElement("- Select available time -");
                model.addElement("Mon 12:00");
                model.addElement("Mon 16:00");
                model.addElement("Tue 12:00");
                model.addElement("Tue 16:00");
                model.addElement("Wed 12:00");
                model.addElement("Wed 16:00");
                model.addElement("Thu 12:00");
                model.addElement("Thu 16:00");
                model.addElement("Fri 12:00");
                model.addElement("Fri 16:00");
                cmbMATeacherTime.setModel(model);
            }
            
            if(cmbMATeacher.getSelectedItem().toString().equals("Waraphan Sarasureeporn")) {
                lblMATeacherPic.setIcon(imageWaraphan);
                DefaultComboBoxModel model = new DefaultComboBoxModel();
                model.addElement("- Select available time -");
                model.addElement("Tue 16:00");
                model.addElement("Wed 10:00");
                model.addElement("Wed 16:00");
                model.addElement("Thu 09:00");
                cmbMATeacherTime.setModel(model);
            }
            
            if(cmbMATeacher.getSelectedItem().toString().equals("Weena Janratchakool")) {
                lblMATeacherPic.setIcon(imageWeena);
                DefaultComboBoxModel model = new DefaultComboBoxModel();
                model.addElement("- Select available time -");
                model.addElement("Fri 12:00");
                cmbMATeacherTime.setModel(model);
            }
            
            if(cmbMATeacher.getSelectedItem().toString().equals("- Select teacher -")) {
                lblMATeacherPic.setIcon(null);
                DefaultComboBoxModel model = new DefaultComboBoxModel();
                model.addElement("- Select teacher first -");
                cmbMATeacherTime.setModel(model);
            }
        }
    }//GEN-LAST:event_cmbMATeacherItemStateChanged

    private void btnClearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnClearMouseClicked
        //clear appointer detail
        txtMAName.setText(null);
        txtMASurname.setText(null);
        txtMAToBeCalledName.setText("(Optional)");
        cmbMAFaculty.setSelectedItem("- Select faculty -");
        txtMAMajor.setText(null);
        cmbMAPosition.setSelectedItem("- Select position -");
        txtMAPhoneNumber.setText(null);
        txtMAEmail.setText("(Optional)");
        txtMALINEID.setText("(Optional)");
        
        //clear appointment detail
        cmbMATeacher.setSelectedItem("- Select teacher -");
        
        if(radMATeacherTime.isSelected() || radMAMyTime.isSelected()) {
            buttonGroup1.clearSelection();
        }
        
        cmbMATeacherTime.setSelectedItem("- Select teacher first -");
        txtMAMyTime.setText("(Example : Wed 13:30)");
        txtMADuration.setText(null);
        txtMAAppointmentDetail.setText(null);
        chkMAUrgent.setSelected(false);
        
    }//GEN-LAST:event_btnClearMouseClicked

