    private void btnNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNextMouseClicked
        ReviewAppointment ra = new ReviewAppointment();
        
        //Appointer
        ra.cmbRATeacher.setSelectedItem((String)cmbMATeacher.getSelectedItem());
        ra.txtRAName.setText(txtMAName.getText());
        ra.txtRASurname.setText(txtMASurname.getText());
        ra.txtRAToBeCalledName.setText(txtMAToBeCalledName.getText());
        ra.cmbRAFaculty.setSelectedItem((String)cmbMAFaculty.getSelectedItem());
        ra.txtRAMajor.setText(txtMAMajor.getText());
        ra.cmbRAPosition.setSelectedItem((String)cmbMAPosition.getSelectedItem());
        ra.txtRAPhoneNumber.setText(txtMAPhoneNumber.getText());
        ra.txtRAEmail.setText(txtMAEmail.getText());
        ra.txtRALINEID.setText(txtMALINEID.getText());
        
        //Appointment
        if(radMATeacherTime.isSelected()) {
            DefaultComboBoxModel modelRA = new DefaultComboBoxModel();
            modelRA.addElement(cmbMATeacherTime.getSelectedItem());
            ra.cmbRATeacherTime.setModel(modelRA);
            ra.txtRAMyTime.setText(null);
            ra.txtRAMyTime.setEnabled(false);
            ra.radRATeacherTime.setSelected(true);
        }
        else {
            ra.txtRAMyTime.setText(txtMAMyTime.getText());
            DefaultComboBoxModel modelRA = new DefaultComboBoxModel();
            modelRA.addElement(null);
            ra.cmbRATeacherTime.setModel(modelRA);
            ra.cmbRATeacherTime.setEnabled(false);
        }
        
        ra.txtRADuration.setText(txtMADuration.getText());
        ra.txtRAAppointmentDetail.setText(txtMAAppointmentDetail.getText());
        
        if(chkMAUrgent.isSelected()) {
            ra.chkRAUrgent.setSelected(true);
        }
        else {
            ra.chkRAUrgent.setSelected(false);
        }
        
        if(cmbMATeacher.getSelectedItem().toString().equals("Khongthep Boonmee")) {
            ra.lblRATeacherPic.setIcon(imageKhongthep);
        }
        
        if(cmbMATeacher.getSelectedItem().toString().equals("Uraiwan Inyaem")) {
            ra.lblRATeacherPic.setIcon(imageUraiwan);
        }
        
        if(cmbMATeacher.getSelectedItem().toString().equals("Waraphan Sarasureeporn")) {
            ra.lblRATeacherPic.setIcon(imageWaraphan);
        }
        
        if(cmbMATeacher.getSelectedItem().toString().equals("Weena Janratchakool")) {
            ra.lblRATeacherPic.setIcon(imageWeena);
        }
        
        ra.show();
        dispose();
    }//GEN-LAST:event_btnNextMouseClicked

